class FlatIterator:
    def __init__(self, list_of_list):
        self.stack = [[list_of_list, 0]]

    def __iter__(self):
        return self

    def __next__(self):
        while self.stack:
            list_of_list, i = self.stack[-1]
            if i < len(list_of_list):
                self.stack[-1][1] += 1
                if not isinstance(list_of_list[i], list):
                    return list_of_list[i]
                self.stack.append([list_of_list[i], 0])
            else:
                self.stack.pop()
        raise StopIteration


def test_1():

    list_of_lists_1 = [
        ['a', 'b', 'c'],
        ['d', 'e', 'f', 'h', False],
        [1, 2, None]
    ]

    for flat_iterator_item, check_item in zip(
            FlatIterator(list_of_lists_1),
            ['a', 'b', 'c', 'd', 'e', 'f', 'h', False, 1, 2, None]
    ):

        assert flat_iterator_item == check_item

    assert list(FlatIterator(list_of_lists_1)) == ['a', 'b', 'c', 'd', 'e', 'f', 'h', False, 1, 2, None]

